class Hulu{
	private int key;
	private String rank;
	private String color;
	public Hulu(int key,String rank,String color){
		this.key=key;
		this.rank=rank;
		this.color=color;
	}
	public int getKey(){
		return this.key;
	}
	public String getRank(){
		return this.rank;
	}
	public String getColor(){
		return this.color;
	}
}
